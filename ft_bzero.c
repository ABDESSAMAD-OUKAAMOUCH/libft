#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*b;

	if (s == NULL)
		return ;
	b = (unsigned char *)s;
	while (n > 0)
	{
		*b = 0;
		b++;
		n--;
	}
}
